package edu.lmu.cs.maze.model;

/**
 * A MazeCell represents a single "square" in a maze.
 * 
 * @author   ray
 * @author   dondi
 */
public enum MazeCell {
    /**
     * Indicates an open location in a maze.
     */
    OPEN,
    
    /**
     * Indicates a walled-in (closed) location in a maze.
     */
    WALL
}
